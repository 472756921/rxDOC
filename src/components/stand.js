export const getStaus = (age, value, type) => {
  let index = getM(age);
  let dataSou = '';
  if(type % 2 == 1) {
  //男性
    if(type == 3) {dataSou = boyHeight};
    if(type == 5) {dataSou = boyWeight};
    if(type == 7) {dataSou = boyHead};
  } else if (type % 2 == 0){
  //女性
    if(type == 2) {dataSou = girlHeight};
    if(type == 4) {dataSou = girlWeight};
    if(type == 6) {dataSou = girlHead};
  }
  let acre = value - dataSou[0][index];
  return parseInt(acre / dataSou[1][index]);
};

function getM(age) {
  let ins = 0;
  m.map((i, index)=> {
    if(age >= i) {
      ins = index;
    }
  })
  return ins;
}
